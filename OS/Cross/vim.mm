  <body>
    <p>
      <b>tabstop</b>&#xa0;Set tabstop to tell vim how many columns a tab counts for.
    </p>
    <p>
      &#xa0;&#xa0;- Linux kernel code expects each tab to be eight columns wide.
    </p>
    <p>
      &#xa0;&#xa0;- Visual Studio expects each tab to be four columns wide.
    </p>
    <p>
      This is the only command here that will affect how existing text displays.&#xa0;&#xa0;
    </p>
    <p>
      
    </p>
    <p>
      <b>expandtab</b>&#xa0;When expandtab is set, hitting Tab in insert mode will produce the appropriate number of spaces.&#xa0;&#xa0;
    </p>
    <p>
      
    </p>
    <p>
      <b>shiftwidth</b>&#xa0;Set shiftwidth to control how many columns text is indented with the reindent operations (&lt;&lt; and &gt;&gt;) and automatic C-style indentation.&#xa0;&#xa0;
    </p>
    <p>
      
    </p>
    <p>
      <b>softtabstop</b>&#xa0;Set softtabstop to control how many columns vim uses when you hit Tab in insert mode. If softtabstop is less than <i><b>tabstop</b></i>&#xa0; and <i><b>expandtab</b></i>&#xa0;is not set, vim will use a combination of tabs and spaces to make up the desired spacing. If <b>softtabstop</b>&#xa0;equals <i><b>tabstop</b></i>&#xa0; and <i><b>expandtab</b></i>&#xa0;is not set, vim will always use tabs. When expandtab is set, vim will always use the appropriate number of spaces.
    </p>
  </body>
</html></richcontent>
